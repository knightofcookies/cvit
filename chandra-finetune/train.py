    model_args, data_args, training_args = parser.parse_args_into_dataclasses()

    local_rank = training_args.local_rank
    os.makedirs(training_args.output_dir, exist_ok=True)

    # 1. Configure 4-bit Quantization
    bnb_config = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_use_double_quant=True,
        bnb_4bit_quant_type="nf4",
        bnb_4bit_compute_dtype=torch.float16,
    )

    # 2. Load the Model
    model = Qwen3VLForConditionalGeneration.from_pretrained(
        model_args.model_name_or_path,
        cache_dir=training_args.cache_dir,
        attn_implementation=attn_implementation,
        torch_dtype=torch.float16,  # Force Float16 for T4
        quantization_config=bnb_config,  # Enable 4-bit loading
    )

    data_args.model_type = "qwen3vl"

    # 3. CRITICAL: Prepare model for k-bit training
    # This freezes weights, casts layer norms to fp32, and enables gradient checkpointing
    model = prepare_model_for_kbit_training(model)

    print(f"Initialized model: {model_args.model_name_or_path}")

    # 4. Load Processor
    processor = AutoProcessor.from_pretrained(
        model_args.model_name_or_path,
    )

    # 5. Apply Monkey Patch for Attention
    if data_args.data_flatten or data_args.data_packing:
        replace_qwen2_vl_attention_class()

    model.config.use_cache = False

    # 6. Apply LoRA (MANDATORY for 4-bit training)
    if training_args.lora_enable:
        print("Applying LoRA...")

        # Target modules for Qwen2.5-VL
        # We target the attention projections and the MLP layers
        target_modules = [
            "q_proj",
            "k_proj",
            "v_proj",
            "o_proj",
            "gate_proj",
            "up_proj",
            "down_proj",
        ]

        lora_config = LoraConfig(
            r=training_args.lora_r,
            lora_alpha=training_args.lora_alpha,
            target_modules=target_modules,
            lora_dropout=training_args.lora_dropout,
            bias="none",
            task_type=TaskType.CAUSAL_LM,
            modules_to_save=[],  # Save nothing extra to save RAM
        )

        model = get_peft_model(model, lora_config)
        model.print_trainable_parameters()
    else:
        # If not using LoRA, this will almost certainly OOM on T4, but leaving logic here
        set_model(model_args, model)

    # 7. Enable Input Gradients (Required for Gradient Checkpointing)
    if hasattr(model, "enable_input_require_grads"):
        model.enable_input_require_grads()
    else:

        def make_inputs_require_grad(module, input, output):
            output.requires_grad_(True)

        model.get_input_embeddings().register_forward_hook(make_inputs_require_grad)

    # Explicitly enable gradient checkpointing again to be safe
    model.gradient_checkpointing_enable()

    # 8. Tokenizer Setup
    tokenizer = transformers.AutoTokenizer.from_pretrained(
        model_args.model_name_or_path,
        cache_dir=training_args.cache_dir,
        model_max_length=training_args.model_max_length,
        padding_side="right",
        use_fast=False,
    )

    # 9. Initialize Trainer
    data_module = make_supervised_data_module(processor, data_args=data_args)
    trainer = Trainer(
        model=model, processing_class=tokenizer, args=training_args, **data_module
    )

    # 10. Start Training
    if list(pathlib.Path(training_args.output_dir).glob("checkpoint-*")):
        logging.info("checkpoint found, resume training")
        trainer.train(resume_from_checkpoint=True)
    else:
        trainer.train()

    trainer.save_state()

    # 11. Save (Safe method for LoRA)
    model.config.use_cache = True
    safe_save_model_for_hf_trainer(trainer=trainer, output_dir=training_args.output_dir)
    processor.save_pretrained(training_args.output_dir)


